public class WilliamAshfordAS2d {

    public String loops()
    {
        int[] arr = {1,2,3,4};
        int j = 0;
        //Loops that loops to the end of the array then returns the number of iterations
        while(j < arr.length)
        {
            j++;
        }
        int i = 0;
        //Loop that counts to 100
        while(i < 100 )
        {
            i++;
        }
        return "Loop 1: " + j + ". Loop2 I counted to " + i;
    }
}
